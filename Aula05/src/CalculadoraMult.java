
public class CalculadoraMult {
	
	// neste método ele irá retornar a soma;
	// este comportamento ira servir de procedimento,  
	// ou se gerarao processos e retorno de valor.
			
		int	obterMult(int a, int b){
			int res = a * b;
			
			return res;
				
			} 

	
		void imprimirMult(int a, int b){
			int res = a * b;
		
		System.out.printf("%d x %d = %d", a, b, res);
		}
	
}
